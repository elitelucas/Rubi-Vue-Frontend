<script lang="ts" setup>
import { useTheme } from 'vuetify/lib/framework.mjs'
import navItems from '@/navigation/horizontal'
import { useThemeConfig } from '@core/composable/useThemeConfig'
import { themeConfig } from '@themeConfig'
import http from "@/utils/http";

// Components
import Footer from '@/layouts/components/Footer.vue'
import NavbarThemeSwitcher from '@/layouts/components/NavbarThemeSwitcher.vue'
import UserProfile from '@/layouts/components/UserProfile.vue'
import logoDark from '@images/logo-dark.svg?raw'
import { HorizontalNavLayout } from '@layouts'
import { VNodeRenderer } from '@layouts/components/VNodeRenderer'
import { useModuleStore } from '@/store/module'


const moduleStore = useModuleStore()


const { appRouteTransition } = useThemeConfig()

const workspace = ref('BitJar Labs Workspace')

const theme = useTheme()

const darkNode = h('div', {
  innerHTML: logoDark,
  style: 'line-height:0; color: rgb(var(--v-global-theme-primary))',
})



onMounted(async () => {
  try {
    const response = await http.get(
      "/v1/subscriptions?order_col=id",
      {},
      {
        headers: {
          "Content-Type": "application/json",
        },
      }
    );
    let resdata = response.data?.data;

    let newsubitems = [];
    resdata.map((item) =>
      newsubitems.push({
        title: item.name,
        to: {
          name: "account-settings",
          params: {
            id: item.id,
          },
        },
      })
    );

    const foundItem = navItems.find((item) => item.title === "My Accounts");
    if (foundItem) {
      foundItem.children = newsubitems;
    }
  } catch (error) {
    console.error("Error fetching data:", error);
  }
});
</script>

<template>
  <HorizontalNavLayout :nav-items="navItems">
    <!-- ðŸ‘‰ navbar -->
    <template #navbar>
      <RouterLink
        to="/"
        class="app-logo d-flex align-center gap-x-3"
      >
        <VNodeRenderer
          :nodes="
            theme.global.current.value.dark ? darkNode : themeConfig.app.logo
          "
        />
      </RouterLink>
      <VCol lg="2">
        <AppSelect
          v-model="workspace"
          :items="['BitJar Labs Workspace', 'BitJar Labs Workspace2']"
          class="app-select-header"
          base-color="primary"
          color="primary"
          item-color="primary"
        />
      </VCol>

      <VSpacer />

      <AppUserBalanceAccount />

      <NavbarThemeSwitcher class="me-2" />

      <IconBtn
        class="me-2"
        @click="() => moduleStore.setModal(true)"
      >
        <VIcon
          size="26"
          class="cursor-pointer"
          icon="tabler-layout-grid-add"
        />
      </IconBtn>
      <UserProfile />
    </template>

    <!-- ðŸ‘‰ Pages -->
    <RouterView v-slot="{ Component }">
      <Transition
        :name="appRouteTransition"
        mode="out-in"
      >
        <Component :is="Component" />
      </Transition>
    </RouterView>

    <!-- ðŸ‘‰ Footer -->
    <template #footer>
      <Footer />
    </template>

    <!-- ðŸ‘‰ Customizer -->
    <!-- <TheCustomizer /> -->
  </HorizontalNavLayout>
</template>

